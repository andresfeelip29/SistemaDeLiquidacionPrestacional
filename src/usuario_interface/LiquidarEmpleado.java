    private void guardarLiquidacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarLiquidacionActionPerformed
        /**
         * En el este if se verifica si al usuario ya se le ha generado una
         * liquidacion usando atributo estadoLiquidacion que almacena un valor
         * false si no se le ha generado, y verdad si de lo contrario
         */
        if (li.getEmpleados(poscision).isEstadoLiquidacion() == false) {

            /**
             * Se hace el llamdo al metodo generarLiquidacion que almacena los
             * valor en los respectivos atributos del empleado
             */
            li.generarLiquidacion(poscision);
            idEmpleado.setEnabled(true);
            fechaRetiro.setEnabled(false);
            precargarLiquidacion.setEnabled(false);
            guardarLiquidacion.setEnabled(false);
            idEmpleado.setText("");
            nombre.setText("");
            sueldo.setText("");
            fechaIngreso.setText("");
            fechaRetiro.setText("");
            primaServicio.setText("");
            cesantias.setText("");
            interesesCesantia.setText("");
            vacaciones.setText("");
            totalLiquidacion.setText("");
            JOptionPane.showMessageDialog(null, "Liquidacion generada con exito");
        } else {
            JOptionPane.showMessageDialog(null, "UPSS.. Usario ya se le genero la liquidacion: intento con otro");
            precargarLiquidacion.setEnabled(false);
            guardarLiquidacion.setEnabled(false);
            idEmpleado.setText("");
            nombre.setText("");
            sueldo.setText("");
            fechaIngreso.setText("");
            fechaRetiro.setText("");
            primaServicio.setText("");
            cesantias.setText("");
            interesesCesantia.setText("");
            vacaciones.setText("");
            totalLiquidacion.setText("");
        }
    }//GEN-LAST:event_guardarLiquidacionActionPerformed

    private void cargarEnpleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cargarEnpleadoActionPerformed
        try {
            /**
             * Se realiza un llamdo al metodo buscar para que devuela la poscion
             * en el arreglo de el valor buscado si retorna la poscion correcta
             * muestra los valores de este arreglo, si no devuelve -1 luego se
             * valida para mostrar la informacion en la ventana en sus
             * respectivos campos
             */
            poscision = li.buscarEmpleado(Integer.parseInt(idEmpleado.getText()));
            if (poscision != - 1) {
                nombre.setText(li.getEmpleados(poscision).getNombre());
                sueldo.setText("$ " + numberFormat.format(li.getEmpleados(poscision).getSalario()));
                fechaIngreso.setText(df.format(li.getEmpleados(poscision).getFechaIngreso()));
                fechaRetiro.setEnabled(true);
                precargarLiquidacion.setEnabled(true);
            } else {
                JOptionPane.showMessageDialog(null, "identificacion de empleado no encontrada por favor intente nuevamente");
                fechaRetiro.setEnabled(false);
                precargarLiquidacion.setEnabled(false);
                nombre.setText("");
                sueldo.setText("");
                fechaIngreso.setText("");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, " Por favor ingrese un numero valido");
        }

    }//GEN-LAST:event_cargarEnpleadoActionPerformed

