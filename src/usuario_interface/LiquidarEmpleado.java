    private void precargarLiquidacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_precargarLiquidacionActionPerformed
        /**
         * Se llaman a los metodo encargados de calcular la seguridad social
         * basandose en la poscion del empleado buscado
         */
        int dias;

        try {
            dias = li.diasTrabajados(df.parse(fechaRetiro.getText()), poscision);
            if (dias >= 0) {
                primaServicio.setText("$ " + numberFormat.format(li.calcularPrimaDeServicio(poscision)));
                cesantias.setText("$ " + numberFormat.format(li.calcularCesantias(poscision)));
                interesesCesantia.setText("$ " + numberFormat.format(li.calcularInteresesCesantias(poscision)));
                vacaciones.setText("$ " + numberFormat.format(li.calcularVacaciones(poscision)));
                totalLiquidacion.setText("$ " + numberFormat.format(li.totalLiquidacion(poscision)));
                guardarLiquidacion.setEnabled(true);
            } else {
                JOptionPane.showMessageDialog(null, "Fecha de retiro anterior a fecha de ingreso por favor validar");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Verifique la fecha de retiro ingresada");
        }
    }//GEN-LAST:event_precargarLiquidacionActionPerformed

